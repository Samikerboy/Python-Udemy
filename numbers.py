#importa otros documentos en este caso math y los trae aqui
from math import *

my_num = -5
print(my_num)

#str = convierte una variable de otro tipo a string
print(str(my_num) + " my favorite number")

#abs = convierte el valor de un número en su valor absoluto
print(abs(my_num))

#pow = convierte el primer numero y lo multiplica a la potencia del segundo ejemplo: 3 a la 3 = 27
print(pow(3, 2))

#max = te dice cual es el numero más grande de los que estan puestos ahi
print(max(3, 2))

#min = te dice cual es el numero más pequeño de los que estan puestos ahi
print(min(3, 2))

#round = redondea los numeros
print(round(3.7))

#floor = redondea los numeros a su numero inicial en este caso 3
print(floor(3.7))

#ceil = redondea los numeros a su numero mas alto en este caso 4
print(ceil(3.1))